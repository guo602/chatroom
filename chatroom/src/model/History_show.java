package model;

public class History_show {
    String uname;
    String time;
    String word;
	
	public String getName() {
		return uname;
	}
	public void setName(String Name) {	
		this.uname = Name;
	}
	public String getWord() {
		return word;
	}
	public void setWord(String Word) {	
		this.word = Word;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String Time) {	
		this.time = Time;
	}
}
